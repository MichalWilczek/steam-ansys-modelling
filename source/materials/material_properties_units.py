
class MaterialPropertiesUnits(object):
    """
    Class that stores all material properties units as strings
    """
    thermal_conductivity_unit = r'$\frac{W}{K \cdot m}$'
    volumetric_heat_capacity_unit = r'$\frac{J}{m^3 \cdot K}$'
    thermal_diffusivity_unit = r'$\frac{m^2}{s}$'
    electrical_resistivity_unit = r'${Ohm} \cdot {m}$'
    power_density_unit = r'$\frac{W}{m^3}$'
